/**
 * <p>
 * The `Point` class represents a single point or coordinate on the game grid,
 * defined by its `x` (horizontal) and `y` (vertical) positions. It is used
 * to track the position of individual blocks or shapes within the grid.
 * </p>
 */
public class Point {
    public int x;
    public int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
